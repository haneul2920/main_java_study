package section03;

public class EX03_13 {

	public static void main(String[] args) {
		// 관계 연산자 : A < B, A <=B, ==(같다면), !=(다르다면)
		int a = 10;
		int b = 20;
		
		System.out.println(a > b);
		System.out.println(a <= b);
		System.out.println(a == b);
		System.out.println(a != b);
		
		System.out.println("--b : "+ --b);
		System.out.println(a != --b);
		
		
		
								
		

	}

}
